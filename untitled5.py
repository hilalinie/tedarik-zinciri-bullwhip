# Bullwhip Etkisi Simülasyonu
np.random.seed(42)
T = 52  # 52 hafta

mean_demand = df['Number of products sold'].mean() / 52
std_demand  = df['Number of products sold'].std()  / 52
mean_lt     = df['Lead times'].mean()

print(f"Gerçek veriden parametreler:")
print(f"  Haftalık ortalama talep : {mean_demand:.1f}")
print(f"  Talep std sapması       : {std_demand:.1f}")
print(f"  Ortalama temin süresi   : {mean_lt:.1f} gün")

# Tüketici talebi
trend    = np.linspace(0, 10, T)
seasonal = 15 * np.sin(2 * np.pi * np.arange(T) / 26)
noise    = np.random.normal(0, std_demand*0.3, T)
consumer_demand = np.maximum(0, mean_demand + trend + seasonal + noise)

def simulate_echelon(upstream_orders, lt, alpha=0.3, safety_factor=1.5):
    orders    = np.zeros(T)
    inventory = np.zeros(T)
    inventory[0] = mean_demand * lt * safety_factor
    for t in range(1, T):
        if t == 1:
            demand_forecast = upstream_orders[t-1]
        else:
            demand_forecast = alpha * upstream_orders[t-1] + (1-alpha) * np.mean(upstream_orders[max(0,t-5):t])
        target_inv  = demand_forecast * lt * safety_factor
        orders[t]   = max(0, demand_forecast + target_inv - inventory[t-1])
        received    = orders[max(0, t-int(lt))] if t > lt else 0
        inventory[t]= max(0, inventory[t-1] - upstream_orders[t-1] + received)
    return orders, inventory

retailer_orders,     retailer_inv     = simulate_echelon(consumer_demand,    1.0)
wholesaler_orders,   wholesaler_inv   = simulate_echelon(retailer_orders,    2.0)
manufacturer_orders, manufacturer_inv = simulate_echelon(wholesaler_orders,  3.0)
supplier_orders,     supplier_inv     = simulate_echelon(manufacturer_orders, 4.0)

def bullwhip_ratio(orders, demand):
    return np.var(orders) / np.var(demand) if np.var(demand) > 0 else 1

bwe_retailer     = bullwhip_ratio(retailer_orders,     consumer_demand)
bwe_wholesaler   = bullwhip_ratio(wholesaler_orders,   consumer_demand)
bwe_manufacturer = bullwhip_ratio(manufacturer_orders, consumer_demand)
bwe_supplier     = bullwhip_ratio(supplier_orders,     consumer_demand)

