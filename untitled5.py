# Normalize metrics to calculate performance score (0-100)
# Higher revenue is better, lower defect rate/lead time/cost are better
max_revenue = supplier_metrics['Ort_Revenue'].max()
min_revenue = supplier_metrics['Ort_Revenue'].min()
max_defect = supplier_metrics['Ort_Defect_Rate'].max()
min_defect = supplier_metrics['Ort_Defect_Rate'].min()

epsilon = 1e-6 # To prevent division by zero

supplier_metrics['Norm_Revenue'] = (supplier_metrics['Ort_Revenue'] - min_revenue) / (max_revenue - min_revenue + epsilon)
supplier_metrics['Norm_Defect_Rate_Inv'] = (1 - (supplier_metrics['Ort_Defect_Rate'] - min_defect) / (max_defect - min_defect + epsilon))

# Combine normalized metrics for a 'Performans_Skoru' (e.g., 60% revenue, 40% inverse defect rate)
supplier_metrics['Performans_Skoru'] = (supplier_metrics['Norm_Revenue'] * 0.6 + supplier_metrics['Norm_Defect_Rate_Inv'] * 0.4) * 100
supplier_metrics['Performans_Skoru'] = supplier_metrics['Performans_Skoru'].round(2)

